public class IntegerDouble {
    public static void main(String[] args) {
        // Declare an integer variable and a double variable and assign numbers to each
        int numInt = 10;
        double numDouble = 1.25;

        // Add the two numbers and assign the sum to a variable
        double sum = numInt + numDouble;

        // Print out the result
        System.out.println("The sum of " + numInt + " and " + numDouble + " is " + sum);
    }
}
